#Comparision operators
#Equal operator ==
print('Equal Comparison')

x = 10
y = 12
print(x == y)

a, b = 4, 4
print(a == b)

print('------------------------')

#Not Equal operator !=
print('Not Equal Comparison')

x = 10
y = 12
print(x != y)

a, b = 4, 4
print(a != b)

print('---------------------------')

#Greater Than operator >
print('Greater Than Comparison')

x = 10
y = 12
print(x > y)

a, b = 16, 11
print(a > b)

print('--------------------------')

#Less Than operator <
print('Less Than Comparison')

x = 10
y = 12
print(x < y)

a = 212.32
b = 212.33
print(a < b)

print('----------------------------')

#Greater Than or Equal To >=
print('Greather Than or Equal To')

a = 77
b = 32
print(a >= b)

x, y = 99, 99
print(x >= y)

print('-------------------------------')

#Less Than or Equal To <=
print('Less Than or Equal To')

t = 34
s = 21
print(t <= s)

a = 22
b = 22
print(a <= b)

print('-------------------------------')

#Logical Operators = and, or, not
print('Logical Operators - AND')

a = 4
print(a > 2 and a < 7)

a = 4
print(a > 2 and a < 4)

print('----------------------------')
print('Logical Operators - OR')

a = 4
print(a > 5 or a < 3)

a = 4
print( a > 5 or a < 6)

print('----------------------------')
print('Logical Operators - NOT')

a = 4
print(not(a > 2 and a < 7))

a = 4
print(not(a > 2 and a < 4))
