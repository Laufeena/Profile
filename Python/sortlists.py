colours = ['red', 'orange', 'yellow', 'green', 'blue', 'purple']
#Sorts list A-Z
colours.sort()
print(colours)
print('------------------------------------------')

#Sorts list Z-A
colours.sort(reverse=True)
print(colours)

print('------------------------------------------')

#reverses the lists output
colours.reverse()
print(colours)

print('-------------------------------------------')

